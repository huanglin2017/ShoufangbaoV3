    //修改资料城市
    public static ArrayList<CenterCity> readCenterCity(Context context) {
        ArrayList<CenterCity> list = new ArrayList<CenterCity>();
        try {
            InputStream is = context.getAssets().open("city.txt");
            InputStreamReader ir = new InputStreamReader(is);
            BufferedReader br = new BufferedReader(ir);
            String s = "";
            String str = "";
            while ((s = br.readLine()) != null) {
                str = s;
                String[] city = str.split(",");
                CenterCity c = new CenterCity();
                c.setCid(Integer.parseInt(city[0]));
                c.setName(city[1]);
                c.setSortLetter(city[2]);
                list.add(c);
            }
            br.close();
        } catch (IOException e) {
            return new ArrayList<CenterCity>();
        }
        return list;
    }

    /**
     * 取出文件
     * @param file
     * @param ctx
     * @param fileName
     * @return
     */
    public static String readFileContentStr(File file, Context ctx,
                                            String fileName) {
        String readOutStr = null;
        try {
            FileInputStream fis = ctx.openFileInput(fileName);
            ByteArrayOutputStream ostream = new ByteArrayOutputStream();
            try {
                int len = 0;
                byte[] buffer = new byte[1024];
                while ((len = fis.read(buffer)) != -1) {
                    ostream.write(buffer, 0, len);
                }
                readOutStr = new String(ostream.toByteArray());
            } finally {
                fis.close();
                ostream.close();
            }
        } catch (IOException e) {
            readOutStr = null;
        }

        return readOutStr;
    }


    /**
     * 保存JSON文件
     * @param json
     * @param context
     * @param fileName
     */
    public static void saveJsonFile(String json, Context context,
                                    String fileName) {
        if (context == null) {
            return;
        }
        try {
            File file = context.getDir(fileName, Context.MODE_PRIVATE);
            if (file.length() > 0) {
                file.delete();
            }
            FileOutputStream fos = context.openFileOutput(fileName,
                    Context.MODE_PRIVATE);
            try {
                fos.write(json.getBytes());
                fos.flush();
            } catch (IOException e) {
                e.printStackTrace();
            } finally {
                try {
                    fos.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
    }
